class MobileAppBuilder:
    """Main mobile app builder orchestrator"""
    
    def __init__(self, output_dir: str = "/tmp/infinity-mobile-builds"):
        self.output_dir = Path(output_dir)
        self.output_dir.mkdir(parents=True, exist_ok=True)
    
    async def build_android(
        self,
        web_url: str,
        config: MobileAppConfig
    ) -> MobileBuildResult:
        """Build Android APK from web URL"""
        builder = AndroidAPKBuilder(config)
        try:
            result = await builder.build_apk_with_bubblewrap(web_url, str(self.output_dir))
            if result.success and result.file_path:
                result.download_url = f"/api/mobile/download/{result.build_id}"
            return result
        finally:
            builder.cleanup()
    
    async def build_pwa(
        self,
        project_files: Dict[str, str],
        config: MobileAppConfig
    ) -> MobileBuildResult:
        """Generate PWA assets for a project"""
        start_time = datetime.now()
        build_id = hashlib.md5(f"{config.app_name}{datetime.now().isoformat()}".encode()).hexdigest()[:12]
        
        try:
            # Create temp project directory
            project_dir = Path(tempfile.mkdtemp(prefix="infinity-pwa-"))
            
            # Write project files
            for path, content in project_files.items():
                file_path = project_dir / path
                file_path.parent.mkdir(parents=True, exist_ok=True)
                file_path.write_text(content)
            
            # Generate PWA assets
            pwa_gen = PWAGenerator(str(project_dir), config)
            files_created = pwa_gen.apply_to_project()
            
            # Create zip package
            output_path = self.output_dir / f"{config.package_name}-{build_id}-pwa.zip"
            
            with zipfile.ZipFile(output_path, 'w', zipfile.ZIP_DEFLATED) as zipf:
                for file_path in project_dir.rglob('*'):
                    if file_path.is_file():
                        arcname = file_path.relative_to(project_dir)
                        zipf.write(file_path, arcname)
            
            # Cleanup
            shutil.rmtree(project_dir)
            
            build_time = (datetime.now() - start_time).total_seconds()
            
            return MobileBuildResult(
                success=True,
                platform="pwa",
                build_type="pwa",
                file_path=str(output_path),
                file_size=output_path.stat().st_size,
                download_url=f"/api/mobile/download/{build_id}",
                build_time_seconds=build_time,
                build_id=build_id,
                manifest=pwa_gen.generate_manifest()
            )
            
        except Exception as e:
            logger.error(f"PWA build error: {e}")
            return MobileBuildResult(
                success=False,
                platform="pwa",
                build_type="pwa",
                error=str(e),
                build_id=build_id
            )
    
    def get_build(self, build_id: str) -> Optional[Path]:
        """Get build file by ID"""
        for file_path in self.output_dir.glob(f"*-{build_id}*"):
            if file_path.is_file():
                return file_path
        return None
    
    def list_builds(self) -> List[Dict]:
        """List all available builds"""
        builds = []
        for file_path in self.output_dir.glob("*"):
            if file_path.is_file():
                builds.append({
                    "filename": file_path.name,
                    "size": file_path.stat().st_size,
                    "created": datetime.fromtimestamp(file_path.stat().st_ctime).isoformat()
                })
        return sorted(builds, key=lambda x: x["created"], reverse=True)


