    """
    iOS IPA Builder (Documentation and Future Implementation)
    
    iOS builds require:
    - macOS with Xcode installed
    - Apple Developer account
    - Valid signing certificates and provisioning profiles
    
    Zero-cost options for iOS:
    1. Codemagic (500 build minutes/month free)
    2. GitHub Actions with macOS runners (limited free minutes)
    3. Local macOS machine
    
    This class provides the interface for future iOS build integration.
    """
    
    def __init__(self, config: MobileAppConfig):
        self.config = config
    
    async def build_ipa(self, project_path: str, output_dir: str) -> MobileBuildResult:
        """
        Build iOS IPA (requires macOS)
        
        For zero-cost iOS builds, users should:
        1. Download the Capacitor package from /api/mobile/build/android
        2. Run on macOS: npx cap add ios && npx cap build ios
        3. Or use Codemagic free tier for cloud builds
        """
        return MobileBuildResult(
            success=False,
            platform="ios",
            build_type="ipa",
            error="iOS builds require macOS. Download the Capacitor package and build locally, or use Codemagic free tier.",
            build_id=""
        )
    
    def generate_xcode_project_config(self) -> Dict:
        """Generate Xcode project configuration"""
        return {
            "PRODUCT_BUNDLE_IDENTIFIER": self.config.package_name,
            "PRODUCT_NAME": self.config.app_name,
            "MARKETING_VERSION": self.config.version,
            "CURRENT_PROJECT_VERSION": str(self.config.version_code),
            "INFOPLIST_KEY_CFBundleDisplayName": self.config.app_name,
            "INFOPLIST_KEY_UILaunchScreen_Generation": "YES",
            "INFOPLIST_KEY_UISupportedInterfaceOrientations": self._get_ios_orientations(),
            "ASSETCATALOG_COMPILER_APPICON_NAME": "AppIcon",
            "ASSETCATALOG_COMPILER_GLOBAL_ACCENT_COLOR_NAME": "AccentColor"
        }
    
    def _get_ios_orientations(self) -> str:
        """Convert orientation config to iOS format"""
        if self.config.orientation == "portrait":
            return "UIInterfaceOrientationPortrait"
        elif self.config.orientation == "landscape":
            return "UIInterfaceOrientationLandscapeLeft UIInterfaceOrientationLandscapeRight"
        else:
            return "UIInterfaceOrientationPortrait UIInterfaceOrientationLandscapeLeft UIInterfaceOrientationLandscapeRight"
    
    def generate_info_plist_entries(self) -> Dict:
        """Generate Info.plist entries"""
        return {
            "CFBundleName": self.config.app_name,
            "CFBundleDisplayName": self.config.app_name,
            "CFBundleIdentifier": self.config.package_name,
            "CFBundleVersion": str(self.config.version_code),
            "CFBundleShortVersionString": self.config.version,
            "UIStatusBarStyle": "UIStatusBarStyleLightContent",
            "UIViewControllerBasedStatusBarAppearance": False,
            "ITSAppUsesNonExemptEncryption": False
        }


