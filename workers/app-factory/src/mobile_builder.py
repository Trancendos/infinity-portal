@dataclass
class MobileAppConfig:
    """Configuration for mobile app generation"""
    app_name: str
    package_name: str  # e.g., com.example.myapp
    version: str = "1.0.0"
    version_code: int = 1
    display_mode: str = "standalone"  # standalone, fullscreen, minimal-ui
    orientation: str = "any"  # any, portrait, landscape
    theme_color: str = "#1a1a2e"
    background_color: str = "#1a1a2e"
    icon_url: Optional[str] = None
    splash_color: str = "#1a1a2e"
    start_url: str = "/"
    scope: str = "/"
    description: str = ""
    short_name: Optional[str] = None
    

@dataclass
class MobileBuildResult:
    """Result of mobile app build"""
    success: bool
    platform: str  # android, ios, pwa
    build_type: str  # apk, aab, ipa, pwa
    file_path: Optional[str] = None
    file_size: Optional[int] = None
    download_url: Optional[str] = None
    error: Optional[str] = None
    build_time_seconds: float = 0
    build_id: str = ""
    manifest: Optional[Dict] = None


